    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String rollNo = jTextField1.getText();
        String phy = jTextField2.getText();
        String che = jTextField3.getText();
        String math = jTextField4.getText();
        String electrical = jTextField5.getText();
        String electronic = jTextField6.getText();

        // Check if any text field is empty
        if (rollNo.isEmpty() || phy.isEmpty() || che.isEmpty() || math.isEmpty() || electrical.isEmpty() || electronic.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields");
            return;
        }

        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // Load the MySQL driver
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "root");
                 PreparedStatement checkStmt = con.prepareStatement("SELECT * FROM student WHERE rollNo = ?");
                 PreparedStatement insertStmt = con.prepareStatement("INSERT INTO result (rollNo, phy, che, math, electrical, electronic) VALUES (?, ?, ?, ?, ?, ?)")) {

                // Check if roll number exists
                checkStmt.setString(1, rollNo);
                try (ResultSet rs = checkStmt.executeQuery()) {
                    if (rs.next()) {
                        // Insert result if roll number exists
                        insertStmt.setString(1, rollNo);
                        insertStmt.setString(2, phy);
                        insertStmt.setString(3, che);
                        insertStmt.setString(4, math);
                        insertStmt.setString(5, electrical);
                        insertStmt.setString(6, electronic);

                        insertStmt.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Successfully saved");

                        setVisible(false);
                        new insertNewResult().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(null, "This Roll Number does not exist. Please try again.");
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Connection error: " + e.getMessage());
        }


        
        
    }//GEN-LAST:event_jButton6ActionPerformed

