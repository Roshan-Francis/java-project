    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        // Assuming you have the following text fields for input
        String rollNo = jTextField1.getText().trim();
        String phy = jTextField2.getText().trim();
        String che = jTextField3.getText().trim();
        String math = jTextField4.getText().trim();
        String electrical = jTextField5.getText().trim();
        String electronic = jTextField6.getText().trim();

        // Check if any text field is empty
        if (rollNo.isEmpty() || phy.isEmpty() || che.isEmpty() || math.isEmpty() || electrical.isEmpty() || electronic.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields");
            return;
        }

        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // Load the MySQL driver
            String url = "jdbc:mysql://localhost:3306/srm";
            String user = "root";
            String password = "root";

            try (Connection con = DriverManager.getConnection(url, user, password);
                 PreparedStatement checkStmt = con.prepareStatement("SELECT * FROM result WHERE rollNo = ?")) {

                checkStmt.setString(1, rollNo);
                try (ResultSet rs = checkStmt.executeQuery()) {
                    if (rs.next()) {
                        // Roll number exists, proceed to update
                        try (PreparedStatement updateStmt = con.prepareStatement("UPDATE result SET phy = ?, che = ?, math = ?, electrical = ?, electronic = ? WHERE rollNo = ?")) {
                            updateStmt.setString(1, phy);
                            updateStmt.setString(2, che);
                            updateStmt.setString(3, math);
                            updateStmt.setString(4, electrical);
                            updateStmt.setString(5, electronic);
                            updateStmt.setString(6, rollNo);

                            int rowsAffected = updateStmt.executeUpdate();
                            if (rowsAffected > 0) {
                                JOptionPane.showMessageDialog(null, "Successfully updated");
                            } else {
                                JOptionPane.showMessageDialog(null, "No changes made or error updating the result");
                            }

                            // Optionally, you can refresh the UI or navigate to another window
                            setVisible(false);
                            new insertNewResult().setVisible(true);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "This Roll Number does not exist. Please try again.");
                    }
                }
            }
         } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "SQL error: " + e.getMessage());
         } catch (ClassNotFoundException e) {
                JOptionPane.showMessageDialog(null, "JDBC Driver not found: " + e.getMessage());
         } catch (Exception e) {
               JOptionPane.showMessageDialog(null, "An error occurred: " + e.getMessage());
        }

        
    }//GEN-LAST:event_jButton7ActionPerformed

