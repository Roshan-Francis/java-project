    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String course = (String) jComboBox1.getSelectedItem();
        String branch = (String) jComboBox2.getSelectedItem();
        String rollNo = jTextField1.getText().trim();
        String name = jTextField2.getText().trim();
        String gender = (String) jComboBox3.getSelectedItem();
        String fatherName = jTextField3.getText().trim();
        if (course == null || branch == null || rollNo.isEmpty() || name.isEmpty() || gender == null || fatherName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields");
            return;
        }
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");  // Load the MySQL driver
            String url = "jdbc:mysql://localhost:3306/srm";
            String user = "root";
            String password = "root";
            try (Connection con = DriverManager.getConnection(url, user, password);
                 PreparedStatement checkStmt = con.prepareStatement("SELECT * FROM student WHERE rollNo = ?")) {
                checkStmt.setString(1, rollNo);
                try (ResultSet rs = checkStmt.executeQuery()) {
                    if (rs.next()) {
                        try (PreparedStatement updateStmt = con.prepareStatement(
                            "UPDATE student SET course = ?, branch = ?, name = ?, gender = ?, fatherName = ? WHERE rollNo = ?")) {
                            updateStmt.setString(1, course);
                            updateStmt.setString(2, branch);
                            updateStmt.setString(3, name);
                            updateStmt.setString(4, gender);
                            updateStmt.setString(5, fatherName);
                            updateStmt.setString(6, rollNo);
                            int rowsUpdated = updateStmt.executeUpdate();
                            if (rowsUpdated > 0) {
                                JOptionPane.showMessageDialog(null, "Successfully updated student: " + name + " (Roll No: " + rollNo + ")");
                            } else {
                                JOptionPane.showMessageDialog(null, "No changes made. Please check the details.");
                            }                           
                            setVisible(false);
                            new adminHome().setVisible(true);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "This Roll Number does not exist. Please try again.");
                    }
                }
            }
        } catch (SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(null, "Error updating: this Roll Number already exists.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "SQL error: " + e.getMessage());
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null, "JDBC Driver not found: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An error occurred: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

