    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course = (String) jComboBox1.getSelectedItem();
        String branch = (String) jComboBox2.getSelectedItem();
        String rollNo = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = (String) jComboBox3.getSelectedItem();
        String fatherName = jTextField3.getText();

        if (course == null || branch == null || rollNo.isEmpty() || name.isEmpty() || gender == null || fatherName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields");
            return;
        }
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");  // Load the MySQL driver
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "root");
                 PreparedStatement pst = con.prepareStatement(
                     "INSERT INTO student (course, branch, rollNo, name, gender, fatherName) VALUES (?, ?, ?, ?, ?, ?)")) {

                // Set values for the PreparedStatement
                pst.setString(1, course);
                pst.setString(2, branch);
                pst.setString(3, rollNo);
                pst.setString(4, name);
                pst.setString(5, gender);
                pst.setString(6, fatherName);
                // Execute the query and check if insert was successful
                int rowsInserted = pst.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(null, "Successfully Updated");
                }
                setVisible(false);
                new adminHome().setVisible(true);
            }
        } catch (SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(null, "This Roll Number already exists.");
            setVisible(false);
            new adminHome().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An error occurred: " + e.getMessage());
        }             
    }//GEN-LAST:event_jButton6ActionPerformed

